logger = logging.getLogger("AsimBrain.Dreaming")

DATA_DIR = Path(__file__).parent.parent.parent / "data"
DATA_DIR.mkdir(parents=True, exist_ok=True)
DREAM_LOG = DATA_DIR / "dream_log.jsonl"
DREAM_DB  = DATA_DIR / "vector_memory.db"


# ── MIRROR-SPECIFIC DREAM TYPES ──────────────────────────────────────────────

class DreamType(Enum):
    """Types of dreams generated by the dreaming engine (used by MirrorModule)."""
    INTEGRATION = "integration"
    PATTERN_RECOGNITION = "pattern_recognition"
    CONTRADICTION_RESOLUTION = "contradiction_resolution"
    EVOLUTION = "evolution"


@dataclass
class Dream:
    """A dream generated by the dreaming engine (used by MirrorModule)."""
    dream_type: DreamType
    content: str
    dream_id: str = ""
    timestamp: float = 0.0
    patterns: Dict[str, float] = field(default_factory=dict)
    metadata: Dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        if not self.dream_id:
            self.dream_id = f"dream_{datetime.now().timestamp()}"
        if not self.timestamp:
            self.timestamp = datetime.now().timestamp()

    def to_dict(self) -> Dict[str, Any]:
        return {
            "dream_id": self.dream_id,
            "dream_type": self.dream_type.value,
            "content": self.content,
            "timestamp": self.timestamp,
            "patterns": self.patterns,
            "metadata": self.metadata,
        }


# ── DREAM STATE ───────────────────────────────────────────────────────────────

@dataclass
class DreamCycle:
    cycle_id: str
    started_at: str
    ended_at: Optional[str] = None
    messages_processed: int = 0
    lessons_extracted: int = 0
    memories_pruned: int = 0
    briefing: str = ""
    status: str = "running"


@dataclass
class Lesson:
    topic: str
    summary: str
    source_messages: List[str] = field(default_factory=list)
    confidence: float = 0.7
    created_at: str = field(default_factory=lambda: datetime.utcnow().isoformat())


# ── PATTERN DETECTOR ─────────────────────────────────────────────────────────

TOPIC_KEYWORDS: Dict[str, List[str]] = {
    "coding":     ["python", "javascript", "code", "bug", "function", "api", "database"],
    "health":     ["health", "doctor", "medicine", "exercise", "diet", "symptom", "स्वास्थ्य"],
    "finance":    ["money", "budget", "invest", "tax", "bank", "पैसा", "बचत"],
    "farming":    ["farm", "crop", "soil", "weather", "खेती", "बाली"],
    "education":  ["learn", "study", "school", "teach", "पढाई"],
    "legal":      ["law", "rights", "court", "contract", "कानून"],
    "creative":   ["design", "art", "write", "story", "creative"],
}


def detect_topics(text: str) -> List[str]:
    text_lower = text.lower()
    found = []
    for topic, keywords in TOPIC_KEYWORDS.items():
        if any(kw in text_lower for kw in keywords):
            found.append(topic)
    return found or ["general"]


def extract_lesson(messages: List[Dict]) -> Optional[Lesson]:
    if not messages:
        return None
    combined = " ".join(m.get("content", "") for m in messages[:10])
    topics = detect_topics(combined)
    summary_lines = []
    for m in messages[:5]:
        content = m.get("content", "").strip()
        if content and m.get("role") == "assistant" and len(content) > 30:
            summary_lines.append(content[:200])
    if not summary_lines:
        return None
    return Lesson(
        topic=topics[0],
        summary=f"[{topics[0].upper()}] " + " | ".join(summary_lines[:2]),
        source_messages=[m.get("id", "") for m in messages[:5]],
        confidence=0.75,
    )


# ── MEMORY STORE INTERFACE ────────────────────────────────────────────────────

def _get_recent_messages(hours: int = 8, limit: int = 200) -> List[Dict]:
    try:
        conn = sqlite3.connect(str(DATA_DIR / "asim_users.db"))
        conn.row_factory = sqlite3.Row
        cutoff = (datetime.utcnow() - timedelta(hours=hours)).isoformat()
        rows = conn.execute(
            "SELECT * FROM messages WHERE created_at >= ? ORDER BY created_at DESC LIMIT ?",
            (cutoff, limit)
        ).fetchall()
        conn.close()
        return [dict(r) for r in rows]
    except Exception:
        return []


def _save_lesson_to_memory(lesson: Lesson, user_id: str = "system"):
    try:
        conn = sqlite3.connect(str(DREAM_DB))
        conn.execute("""
            CREATE TABLE IF NOT EXISTS dream_lessons (
                id TEXT PRIMARY KEY,
                user_id TEXT,
                topic TEXT,
                summary TEXT,
                confidence REAL,
                created_at TEXT
            )
        """)
        import secrets
        conn.execute("""
            INSERT INTO dream_lessons (id, user_id, topic, summary, confidence, created_at)
            VALUES (?,?,?,?,?,?)
        """, (secrets.token_hex(6), user_id, lesson.topic,
              lesson.summary, lesson.confidence, lesson.created_at))
        conn.commit()
        conn.close()
    except Exception as e:
        logger.debug(f"Memory save: {e}")


def _prune_old_lessons(days: int = 30) -> int:
    try:
        conn = sqlite3.connect(str(DREAM_DB))
        cutoff = (datetime.utcnow() - timedelta(days=days)).isoformat()
        cursor = conn.execute(
            "DELETE FROM dream_lessons WHERE created_at < ? AND confidence < 0.5",
            (cutoff,)
        )
        count = cursor.rowcount
        conn.commit()
        conn.close()
        return count
    except Exception:
        return 0


def _get_dream_lessons(limit: int = 20) -> List[Dict]:
    try:
        conn = sqlite3.connect(str(DREAM_DB))
        conn.row_factory = sqlite3.Row
        conn.execute("""
            CREATE TABLE IF NOT EXISTS dream_lessons (
                id TEXT PRIMARY KEY,
                user_id TEXT,
                topic TEXT,
                summary TEXT,
                confidence REAL,
                created_at TEXT
            )
        """)
        rows = conn.execute(
            "SELECT * FROM dream_lessons ORDER BY created_at DESC LIMIT ?",
            (limit,)
        ).fetchall()
        conn.close()
        return [dict(r) for r in rows]
    except Exception:
        return []


# ── BRIEFING GENERATOR ────────────────────────────────────────────────────────

def generate_briefing(lessons: List[Lesson], cycle: DreamCycle) -> str:
    if not lessons:
        return (
            f"🌙 Dream Cycle #{cycle.cycle_id[:6]} complete.\n"
            f"Processed {cycle.messages_processed} messages. No new lessons today.\n"
            "System is rested and ready."
        )
    topic_counts: Dict[str, int] = {}
    for l in lessons:
        topic_counts[l.topic] = topic_counts.get(l.topic, 0) + 1
    top_topics = sorted(topic_counts.items(), key=lambda x: x[1], reverse=True)[:3]
    topic_str = ", ".join(f"{t}({c})" for t, c in top_topics)
    return (
        f"🌙 **Dream Cycle Complete** · {datetime.utcnow().strftime('%H:%M UTC')}\n"
        f"📊 Processed: {cycle.messages_processed} msgs · "
        f"Learned: {cycle.lessons_extracted} lessons · "
        f"Pruned: {cycle.memories_pruned} memories\n"
        f"🧠 Top topics: {topic_str}\n"
        f"✅ System memory consolidated. Ready for new day."
    )


# ── DREAM ENGINE ──────────────────────────────────────────────────────────────

class DreamingEngine:
    """
    Background memory consolidation engine.
    Call start() to run as an asyncio background task.
    """

    def __init__(self, cycle_interval_minutes: int = 60, user_id: Optional[str] = None):
        self.cycle_interval = cycle_interval_minutes * 60
        self._running = False
        self._current_cycle: Optional[DreamCycle] = None
        self._last_briefing: str = "Not yet dreamed."
        self._total_cycles: int = 0
        # MirrorModule integration
        self.user_id = user_id or "default"
        self._lock = threading.Lock()
        self._dreams: List[Dream] = []
        self._patterns: Dict[str, float] = {}
        logger.info("🌙 DreamingEngine initialized")

    async def start(self):
        self._running = True
        logger.info("🌙 DreamingEngine started")
        while self._running:
            try:
                await self._run_cycle()
            except Exception as e:
                logger.error(f"Dream cycle error: {e}")
            await asyncio.sleep(self.cycle_interval)

    def stop(self):
        self._running = False
        logger.info("🌙 DreamingEngine stopped")

    async def _run_cycle(self):
        import secrets
        cycle_id = secrets.token_hex(4)
        self._current_cycle = DreamCycle(
            cycle_id=cycle_id,
            started_at=datetime.utcnow().isoformat(),
        )
        logger.info(f"🌙 Dream cycle {cycle_id} starting…")

        # Step 1: Fetch recent messages
        messages = await asyncio.to_thread(_get_recent_messages, 8, 200)
        self._current_cycle.messages_processed = len(messages)

        # Step 2: Group by user and extract lessons
        lessons: List[Lesson] = []
        user_messages: Dict[str, List[Dict]] = {}
        for m in messages:
            uid = m.get("user_id", "anonymous")
            user_messages.setdefault(uid, []).append(m)

        for uid, msgs in user_messages.items():
            lesson = await asyncio.to_thread(extract_lesson, msgs)
            if lesson:
                lessons.append(lesson)
                await asyncio.to_thread(_save_lesson_to_memory, lesson, uid)

        self._current_cycle.lessons_extracted = len(lessons)

        # Step 3: Prune stale memories
        pruned = await asyncio.to_thread(_prune_old_lessons, 30)
        self._current_cycle.memories_pruned = pruned

        # Step 4: Generate briefing
        briefing = generate_briefing(lessons, self._current_cycle)
        self._current_cycle.briefing = briefing
        self._current_cycle.status = "completed"
        self._current_cycle.ended_at = datetime.utcnow().isoformat()
        self._last_briefing = briefing
        self._total_cycles += 1

        # Step 5: Bug triage (subconscious scan)
        bug_stats = await asyncio.to_thread(self._run_bug_triage)

        # Step 6: Write dream log
        await asyncio.to_thread(self._write_log, self._current_cycle)

        # Step 7: Publish cycle-complete event to Reactive Bus
        await asyncio.to_thread(self._emit_cycle_event, cycle_id, len(lessons), pruned, bug_stats)

        logger.info(
            f"🌙 Dream cycle {cycle_id} done — "
            f"{len(messages)} msgs, {len(lessons)} lessons, {pruned} pruned, "
            f"bugs={bug_stats.get('total',0)}"
        )

    def _run_bug_triage(self) -> Dict[str, Any]:
        """Run automated bug triage during dream cycle."""
        try:
            from core.dreaming.bug_triage import get_bug_triage
            engine = get_bug_triage()
            # Simulate scan: check key project files for common issues
            sample_bugs = [
                {"file": "core/dharma/delta_t_engine.py", "line": 0,
                 "category": "logic", "description": "Dream scan: verify none checks on node_states"},
                {"file": "core/mesh/p2p_node.py", "line": 0,
                 "category": "performance", "description": "Dream scan: review async timeout handling"},
            ]
            stats = engine.batch_triage(sample_bugs)
            return {
                "total": stats.total, "critical": stats.critical,
                "high": stats.high, "auto_applied": stats.auto_applied,
                "pending_human": stats.pending_human,
            }
        except Exception as e:
            logger.debug(f"Bug triage skipped: {e}")
            return {"total": 0}

    def _emit_cycle_event(self, cycle_id: str, lessons: int, pruned: int, bug_stats: Dict):
        """Publish dream cycle completion to the Reactive Event Bus."""
        try:
            from core.events.reactive_bus import get_bus
            get_bus().publish(
                topic="dreaming.cycle_complete",
                payload={
                    "cycle_id":  cycle_id,
                    "lessons":   lessons,
                    "pruned":    pruned,
                    "bugs":      bug_stats.get("total", 0),
                    "critical":  bug_stats.get("critical", 0),
                },
                source="dreaming_engine",
            )
            if bug_stats.get("critical", 0) > 0:
                get_bus().publish(
                    topic="dreaming.critical_bugs",
                    payload=bug_stats,
                    source="dreaming_engine",
                )
        except Exception as e:
            logger.debug(f"Event bus emit skipped: {e}")

    def _write_log(self, cycle: DreamCycle):
        try:
            with open(DREAM_LOG, "a", encoding="utf-8") as f:
                f.write(json.dumps({
                    "id": cycle.cycle_id,
                    "started_at": cycle.started_at,
                    "ended_at": cycle.ended_at,
                    "messages_processed": cycle.messages_processed,
                    "lessons_extracted": cycle.lessons_extracted,
                    "memories_pruned": cycle.memories_pruned,
                    "status": cycle.status,
                }) + "\n")
        except Exception:
            pass

    # ── MIRROR-SPECIFIC METHODS ──────────────────────────────────────────────

    async def nightly_evolution(self, reflections: Optional[List[Dict[str, Any]]] = None) -> List[Dream]:
        """Process reflections through dream-like evolution (used by MirrorModule)."""
        dreams: List[Dream] = []
        if reflections:
            for ref in reflections:
                dream = Dream(
                    dream_type=DreamType.INTEGRATION,
                    content=f"Integrated reflection: {ref.get('intent', 'unknown')}",
                    patterns={ref.get('intent', 'unknown'): random.random()},
                )
                with self._lock:
                    self._dreams.append(dream)
                    self._patterns[dream.content] = 1.0
                dreams.append(dream)
        return dreams

    def get_patterns(self) -> Dict[str, float]:
        """Get the current pattern dictionary (used by MirrorModule)."""
        with self._lock:
            return dict(self._patterns)

    # ── PUBLIC API ────────────────────────────────────────────────────────────

    def status(self) -> Dict[str, Any]:
        return {
            "running": self._running,
            "total_cycles": self._total_cycles,
            "last_briefing": self._last_briefing,
            "current_cycle": self._current_cycle.cycle_id if self._current_cycle else None,
            "cycle_interval_minutes": self.cycle_interval // 60,
        }

    def get_briefing(self) -> str:
        return self._last_briefing

    def get_recent_lessons(self, limit: int = 10) -> List[Dict]:
        return _get_dream_lessons(limit)

    async def trigger_now(self) -> str:
        await self._run_cycle()
        return self._last_briefing


