@router.post("/api/mirror/consciousness/thought")
async def mirror_add_thought(data: dict = Body(...)):
    """Add a thought to the consciousness layer.

    Request body:
      - user_id (str, optional): User ID (default: "system")
      - thought_type (str, required): Type of thought
      - content (str, required): Thought content
      - source (str, optional): Source of the thought

    Returns:
      - thought: The created Thought object
    """
    try:
        from core.mirror.consciousness import ConsciousnessLayer, Thought, ThoughtType

        user_id = data.get("user_id", "system")
        thought_type_str = data.get("thought_type")
        content = data.get("content")
        source = data.get("source", "api")

        if not thought_type_str or not content:
            return error("thought_type and content are required")

        try:
            thought_type = ThoughtType(thought_type_str)
        except ValueError:
            valid_types = [t.value for t in ThoughtType]
            return error(f"Invalid thought_type. Valid: {valid_types}")

        consciousness = ConsciousnessLayer(user_id=user_id)
        thought = Thought(
            thought_type=thought_type,
            content=content,
        )
        consciousness.add_thought(thought)

        return ok(data={
            "thought_id": thought.thought_id,
            "thought_type": thought.thought_type.value,
            "content": thought.content,
            "source": thought.source,
            "timestamp": thought.timestamp,
        })
    except Exception as e:
        logger.exception("Failed to add thought")
        return error(str(e))


