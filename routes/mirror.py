# ─── Reflection ──────────────────────────────────────────────────────────────


@router.post("/api/mirror/reflect")
async def mirror_reflect(data: dict = Body(...)):
    """Reflect on an action and generate a mirror reflection.

    Request body:
      - user_id (str, required): The user performing the action
      - action (dict, required): The action to reflect on
        - intent (str): The intent of the action
        - details (dict): Additional action details
      - user_type (str, optional): User type (default: "citizen")

    Returns:
      - reflection: The generated MirrorReflection with analysis
    """
    try:
        from core.mirror import get_mirror

        user_id = data.get("user_id")
        action = data.get("action")
        user_type = data.get("user_type", "citizen")

        if not user_id or not action:
            return error("user_id and action are required")

        mirror = get_mirror(user_id, user_type)
        reflection = await mirror.reflect(action)

        return ok(data={
            "reflection_id": reflection.reflection_id,
            "user_id": reflection.user_id,
            "intent": reflection.intent,
            "contradictions": reflection.contradictions,
            "balance_impact": reflection.balance_impact,
            "response": reflection.response,
            "timestamp": reflection.timestamp,
        })
    except Exception as e:
        logger.exception("Failed to reflect on action")
        return error(str(e))


